import UIKit

extension UIColor {
    class func noneColor() -> UIColor {
        return UIColor(red:1, green:0.42, blue:0.49, alpha:1)
    }
    
    class func fewColor() -> UIColor {
        return UIColor(red:1, green:0.94, blue:0.23, alpha:1)
    }
    
    class func plentyColor() -> UIColor {
        return UIColor(red:0.6, green:0.92, blue:0.24, alpha:1)
    }
    
    class func flamePeaColor() -> UIColor {
        return UIColor(red:0.76, green:0.39, blue:0.25, alpha:1)
    }
    
    class func summerGreenColor() -> UIColor {
        return UIColor(red:0.53, green:0.72, blue:0.6, alpha:1)
    }
    
    class func blueGrayColor() -> UIColor {
        return UIColor(red:0.39, green:0.64, blue:0.77, alpha:1)
    }
    
    class func concreteColor() -> UIColor {
        return UIColor(red:0.95, green:0.95, blue:0.95, alpha:1)
    }
    
    class func springWoodColor() -> UIColor {
        return UIColor(red:0.97, green:0.96, blue:0.95, alpha:1)
    }
    
    class func jumboColor() -> UIColor {
        return UIColor(red:0.48, green:0.48, blue:0.5, alpha:1)
    }
}